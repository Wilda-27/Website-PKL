    <script>
        // AOS Animation
        AOS.init({
            duration: 800,
            once: true,
            offset: 100
        });

        // Navbar Scroll Effect
        window.addEventListener('scroll', function() {
            const navbar = document.querySelector('.navbar-modern');
            const backToTop = document.getElementById('backToTop');
            
            if (window.scrollY > 100) {
                navbar.classList.add('scrolled');
                backToTop.classList.add('visible');
            } else {
                navbar.classList.remove('scrolled');
                backToTop.classList.remove('visible');
            }
        });

        // Back to Top
        document.getElementById('backToTop').addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });

        // Smooth Scroll
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                if (this.getAttribute('href') === '#') return;
                
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    window.scrollTo({
                        top: target.offsetTop - 80,
                        behavior: 'smooth'
                    });
                }
            });
        });

        // Simple Calendar Navigation
        const calendarNav = document.querySelectorAll('.calendar-nav');
        const calendarMonth = document.querySelector('.calendar-month');
        
        const months = ['Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni', 
                       'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'];
        
        let currentMonth = 2; // March (0-indexed)
        
        calendarNav.forEach(button => {
            button.addEventListener('click', function() {
                if (this.querySelector('.fa-chevron-left')) {
                    currentMonth = (currentMonth - 1 + 12) % 12;
                } else {
                    currentMonth = (currentMonth + 1) % 12;
                }
                calendarMonth.textContent = `${months[currentMonth]} 2025`;
            });
        });

        // Event Card Hover Effect
        document.querySelectorAll('.event-card, .event-card-sm, .past-event-card').forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-5px)';
            });
            
            card.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0)';
            });
        });
    </script>
