        new Glide('#recognitionGlide', {
            type: 'carousel',
            perView: 3,
            gap: 30,
            breakpoints: {
                992: {
                    perView: 2
                },
                768: {
                    perView: 1
                }
            },
            autoplay: 3000,
            hoverpause: true
        }).mount();

        // Team Card Hover Effect
        document.querySelectorAll('.team-card-modern').forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-10px)';
            });
            
            card.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0)';
            });
